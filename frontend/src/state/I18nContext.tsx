type TranslationKey = keyof typeof en;

function interpolate(value: string, params: TranslationParams) {
  return value.replace(/\{(\w+)\}/g, (_, key) => String(params[key] ?? ""));
}

function translate(languageCode: string, key: TranslationKey, params: TranslationParams = {}) {
  const appLanguageCode = getAppLanguageCode(languageCode);
  const activeMessages = appLanguageCode === "pl"
    ? pl
    : appLanguageCode === "de"
      ? de
      : appLanguageCode === "es"
        ? es
      : en;
  const value = activeMessages[key] ?? en[key];

  return typeof value === "function" ? value(params) : interpolate(value, params);
}

type I18nContextValue = {
  languageCode: string;
  setLanguageCode: (languageCode: string) => void;
  t: (key: TranslationKey, params?: TranslationParams) => string;
};

const defaultLanguageCode = "en";

const defaultContextValue: I18nContextValue = {
  languageCode: defaultLanguageCode,
  setLanguageCode: () => undefined,
  t: (key, params) => translate(defaultLanguageCode, key, params)
};

const I18nContext = createContext<I18nContextValue>(defaultContextValue);

function resolveInitialLanguageCode(initialLanguageCode?: string) {
  if (initialLanguageCode) {
    return normalizeLanguageCode(initialLanguageCode);
  }

  if (typeof window === "undefined") {
    return defaultLanguageCode;
  }

  const storedLanguageCode = window.localStorage.getItem(storageKey);
  if (storedLanguageCode) {
    return normalizeLanguageCode(storedLanguageCode);
  }

  return normalizeLanguageCode(window.navigator.language);
}

export function I18nProvider({ children, initialLanguageCode }: { children: ReactNode; initialLanguageCode?: string }) {
  const [languageCode, setLanguageCodeState] = useState(() => resolveInitialLanguageCode(initialLanguageCode));

  const setLanguageCode = useCallback((value: string) => {
    setLanguageCodeState(normalizeLanguageCode(value));
  }, []);

  useEffect(() => {
    setFormattingLanguageCode(languageCode);
    if (typeof document !== "undefined") {
      document.documentElement.lang = languageCode;
    }
    if (typeof window !== "undefined") {
      window.localStorage.setItem(storageKey, languageCode);
    }
  }, [languageCode]);

  const value = useMemo<I18nContextValue>(
    () => ({
      languageCode,
      setLanguageCode,
      t: (key, params) => translate(languageCode, key, params)
    }),
    [languageCode, setLanguageCode]
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  return useContext(I18nContext);
}
